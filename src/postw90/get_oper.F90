    complex(kind=dp), allocatable :: CC_q(:,:,:,:,:)
    complex(kind=dp), allocatable :: Ho_qb1_q_qb2(:,:)
    complex(kind=dp), allocatable :: H_qb1_q_qb2(:,:)
    real(kind=dp)                 :: c_real,c_img
