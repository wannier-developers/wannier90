                  spn_o(:,:,ik,is), SS_q(:,:,ik,is))
          enddo !is
       enddo !ik

       call fourier_q_to_R(SS_q(:,:,:,1),SS_R(:,:,:,1))
       call fourier_q_to_R(SS_q(:,:,:,2),SS_R(:,:,:,2))
