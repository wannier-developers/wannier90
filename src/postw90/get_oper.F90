             call get_win_min(qb2,winmin_qb2)
             do nn1=1,nntot
                qb1=nnlist(ik,nn1)
                call get_win_min(qb1,winmin_qb1)
                !
                ! Read from .uHu file the matrices <u_{q+b1}|H_q|u_{q+b2}> 
                ! between the original ab initio eigenstates
