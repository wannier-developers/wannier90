                CC_q(:,:,ik,b,a)=conjg(transpose(CC_q(:,:,ik,a,b)))
             enddo
          enddo
       enddo !ik

       close(uHu_in)

       do b=1,3
          do a=1,3
             call fourier_q_to_R(CC_q(:,:,:,a,b),CC_R(:,:,:,a,b))
          enddo
       enddo

    endif !on_root
    
    call comms_bcast(CC_R(1,1,1,1,1),num_wann*num_wann*nrpts*3*3)

